def get_seconds_to_next(skill_type: str, level: int) -> int | None:
    lookup = {
        "melee": MELEE_SKILL_SECONDS,
        "shielding": SHIELDING_SECONDS,
        "distance": DISTANCE_SECONDS,
    }.get(skill_type.lower())
    if lookup is None:
        return None
    return lookup.get(level)


def get_distance_brackets(level: int) -> tuple[float | None, float | None, int | None, int | None]:
    """
    Return (seconds_min, seconds_max, stones_min, stones_max) for distance level -> next level.
    Values represent full level (0% -> 100%).
    """
    mins_min = DISTANCE_MINUTES_MIN.get(level)
    mins_max = DISTANCE_MINUTES_MAX.get(level)
    sec_min = mins_min * 60 if mins_min is not None else None
    sec_max = mins_max * 60 if mins_max is not None else None
    stones_min = DISTANCE_STONES_MIN.get(level)
    stones_max = DISTANCE_STONES_MAX.get(level)
    return sec_min, sec_max, stones_min, stones_max
